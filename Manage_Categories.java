    void load_category(){
        al.clear();
        
        String ans=myClient.fetchcat();
        System.out.println(ans);
        StringTokenizer st=new StringTokenizer(ans,";;");
        while(st.hasMoreTokens()){
            String row=st.nextToken();
            StringTokenizer st1=new StringTokenizer(row,"$");
            String name=st1.nextToken();
            String photo=st1.nextToken();
            al.add(new Category(name,photo));
        }
        
    }
class myTableModel extends AbstractTableModel{

        @Override
        public int getRowCount() {
            return al.size();
        }

        @Override
        public int getColumnCount() {
            return 2;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
           if(columnIndex==0)return al.get(rowIndex).name;
           else return al.get(rowIndex).photo;
        }

        @Override
        public String getColumnName(int column) {
            String names[]={"Name","Photo"};
            return names[column];
        }
        
    
}
