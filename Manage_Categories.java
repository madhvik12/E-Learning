    private void BrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BrowseActionPerformed
        // TODO add your handling code here:
        jfc=new JFileChooser();
        int ans=jfc.showOpenDialog(this);
        if(ans==JFileChooser.APPROVE_OPTION){
            ph=jfc.getSelectedFile();
            ImageIcon ic=new ImageIcon(ph.getPath());
            Image img=ic.getImage().getScaledInstance(photolb.getWidth(), photolb.getHeight(), Image.SCALE_SMOOTH);
            ImageIcon ic1=new ImageIcon(img);
            photolb.setIcon(ic1);
            
        }
    }//GEN-LAST:event_BrowseActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String category=catname.getText();
       
        String ans=myClient.addCategory(category,ph);
        if(ans.trim().equals("success")){
            JOptionPane.showMessageDialog(rootPane, "Category added");
            catname.setText("");
            photolb.setIcon(null);
            load_category();
            tm.fireTableDataChanged();
        
        }
        else JOptionPane.showMessageDialog(rootPane, ans);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int ans=mytable.getSelectedRow();
        if(ans==-1){
            JOptionPane.showMessageDialog(rootPane, "Please select a row");
        }
        else{
            String ans1=myClient.deletecat(al.get(ans).name);
            if(ans1.trim().equals("success")){
                JOptionPane.showMessageDialog(rootPane, "Deleted successfully");
                load_category();
                tm.fireTableDataChanged();
                
            }
            else{
                JOptionPane.showMessageDialog(rootPane, ans1);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

